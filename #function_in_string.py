#function_in_string

#Lower / Upper
s = "PyThOn"
print(s.lower())
print(s.upper())

#Replace
s = "I like Java"
print(s.replace("Java", "Python"))

#Count and Find
s = "banana"
print(s.count("a"))
print(s.find("n"))

#Check Type of String
print("123".isdigit())
print("abc".isalpha())
print("abc123".isalnum())

#Remove Spaces
s = "  hello  "
print(s.strip())

#Reverse a String
s = "python"
print(s[::-1])


